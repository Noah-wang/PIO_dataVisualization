def localize_chart_title(title: str, lang: str) -> str:
    mapping = {
        "Monthly Installation Quantity Trend": t(lang, "default_monthly_installation_trend"),
        "Monthly Revenue Trend": t(lang, "default_monthly_revenue_trend"),
        "Top Vehicle Models by Sales": t(lang, "default_top_models"),
        "Top Parts by Revenue": t(lang, "default_top_parts_revenue"),
        "Top Parts by Installation Quantity": t(lang, "default_top_parts_installation"),
    }
    return mapping.get(title, title)
