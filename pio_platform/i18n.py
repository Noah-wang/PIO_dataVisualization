def t(lang: str, key: str, **kwargs: object) -> str:
    template = TRANSLATIONS.get(lang, TRANSLATIONS["en"]).get(key, key)
    return template.format(**kwargs)


def localize_profile_df(profile_df: pd.DataFrame, lang: str) -> pd.DataFrame:
    rename_map = {
        "Column": t(lang, "column"),
        "Type": t(lang, "type"),
        "Missing": t(lang, "missing"),
        "Missing %": t(lang, "missing_pct"),
        "Unique": t(lang, "unique"),
        "Sample Values": t(lang, "sample_values"),
    }
    localized = profile_df.rename(columns=rename_map).copy()
    type_column = t(lang, "type")
    if type_column in localized.columns:
        localized[type_column] = localized[type_column].replace(
            {
                "date": t(lang, "type_date"),
                "numeric": t(lang, "type_numeric"),
                "boolean": t(lang, "type_boolean"),
                "category": t(lang, "type_category"),
            }
        )
    return localized


